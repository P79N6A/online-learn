<script>
	import qs from "qs";
	import axios from "axios";
	export default {
		name: 'Login',
		data(){
			return{
				phone:'',
				pwd:'',
				log:null
			}
		},
		methods: {
			//跳转到注册页面
			toRegister(){
				this.$router.push("/register")
			},
			//点击登录
			login(){
				var _this=this;
				if (this.log!=null) {
					//用户登陆
					if (this.log==1) {
						axios({
							url:'http://localhost:3000/userlogin',
							params:{phoneNum:_this.phone,password:_this.pwd}
						}).then(function(data){
							console.log(data)
							if (data.data.code==0) {
								_this.$refs.phoneT.innerHTML=data.data.msg
							}else{
								sessionStorage.setItem("username",_this.phone);
								_this.$router.push("/home")
							}
						})
					}else{
						//管理员登陆
						axios({
							url:'http://localhost:3000/adminlogin',
							params:{phoneNum:_this.phone,password:_this.pwd}
						}).then(function(data){
							if (data.data.code==0) {
								_this.$refs.phoneT.innerHTML=data.data.msg
							}else{
								sessionStorage.setItem("managename",_this.phone);
								_this.$router.push("/Admin")
							}
						})
					}
				}else{
					alert("请选择角色")
				}
			}
		}
	}
</script>

